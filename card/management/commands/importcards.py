class Command(BaseCommand):
    help = 'Imports cards from json file into db'

    def handle(self, *args, **options):
      with open('card/cards.json') as file:
          cards = json.load(file)['entry']

          for card in cards:
            Card(**{
              'id': card['number'] + '-' + card['name'].lower().replace(' ', '-').replace(',', '').replace('!', ''),
              'number': card['number'],
              'name': card['name'],
              'house': card['house'],
              'type': card['type'],
              'rarity': card['rarity'],
              'aember': int(card['aember']) if card['aember'] else None,
              'power': int(card['power']) if card['power'] else None,
              'armor': int(card['armor']) if card['armor'] else None,
              'traits': card['traits'],
              'keywords': card['keywords'],
              'text': card['text'],
              'imageurl': images[card['name']],
              'artist': card['artist'],
              'setsku': card['setsku'],
            }).save()